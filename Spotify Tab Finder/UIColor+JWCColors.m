#import "UIColor+JWCColors.h"

@implementation UIColor (JWCColors)

+ (UIColor *)gunMetal
{
    return [UIColor colorWithRed:0.12 green:0.21 blue:0.24 alpha:1.0];
}

+ (UIColor *)tealBlue
{
    return [UIColor colorWithRed:0.25 green:0.47 blue:0.55 alpha:1.0];
}

+ (UIColor *)grayBlue
{
    return [UIColor colorWithRed:0.57 green:0.59 blue:0.68 alpha:1.0];
}

+ (UIColor *)darkPurple
{
    return [UIColor colorWithRed:0.18 green:0.12 blue:0.18 alpha:1.0];
}

+ (UIColor *)eerieBlack
{
    return [UIColor colorWithRed:0.09 green:0.06 blue:0.16 alpha:1.0];
}

@end
